/**
 * Icon Browser Data — Complete registry of all icons used across the site.
 * Source: Audited from all lucide-react imports + custom SVG icons.
 * Used by: IconBrowser (/ontwikkelaar/ikone)
 *
 * Categories:
 * - interface: UI chrome icons (navigation, actions, controls, status indicators)
 * - content: Decorative/informational icons within page content (cards, features, etc.)
 * - social: Social media brand icons
 * - both: Used in both interface and content contexts
 */

export type IconType = 'interface' | 'content' | 'social' | 'both';
export type IconSource = 'lucide-react' | 'custom-svg';

/**
 * Semantic category for grouped display in the Icon Browser.
 * Maps to icon-block.md categories and icons.json manifest.
 */
export type IconCategory =
  | 'navigation'      // Chevrons, arrows, menu, back/forward
  | 'actions'         // Edit, copy, download, share, delete
  | 'status'          // Check, alert, warning, info, success
  | 'content'         // Article, calendar, tag, bookmark
  | 'media'           // Camera, video, audio, image
  | 'commerce'        // Cart, credit card, pricing
  | 'social'          // Facebook, Instagram, X, WhatsApp
  | 'communication'   // Mail, phone, chat
  | 'user'            // User, login, account
  | 'layout'          // Grid, list, panel, layout
  | 'misc';           // Everything else

export interface IconUsage {
  /** File where icon is used */
  file: string;
  /** Brief context of how it's used */
  context: string;
  /** Whether used as interface or content in this specific context */
  type: IconType;
}

export interface IconEntry {
  /** Unique icon identifier */
  id: string;
  /** Icon name (as imported) */
  name: string;
  /** Display label */
  label: string;
  /** Package source */
  source: IconSource;
  /** Primary classification */
  type: IconType;
  /**
   * Semantic display category for grouped browsing.
   * Used in icons.json manifest and Icon Browser category filter.
   * GC-051: Part of formalized icon manifest schema.
   */
  category?: IconCategory;
  /**
   * Original Lucide React icon name (for icons derived from Lucide).
   * Undefined for custom SVGs. Null = confirmed non-Lucide.
   * Used in icons.json manifest for WordPress icon block `lucideName` field.
   * GC-051: Required for icons.json manifest schema.
   */
  lucideName?: string | null;
  /** Common sizes used (px) */
  sizes: number[];
  /** Import statement */
  importStatement: string;
  /** JSX usage example */
  jsxExample: string;
  /** WordPress Dashicon equivalent (if any) */
  wpDashicon: string | null;
  /** WordPress equivalent using dashicons or custom */
  wpExport: string;
  /** Where this icon is used */
  usages: IconUsage[];
  /** Tags for searchability */
  tags: string[];
}

